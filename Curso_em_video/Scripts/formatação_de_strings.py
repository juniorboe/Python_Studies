saudacao = str('Bom dia!')
print(f'{saudacao:^20}') #Centraliza a string em 20 caracteres
print(f'{saudacao:>20}') #Posiciona a string a direita, utilizando 20 caracteres de espaço
print(f'{saudacao:<20}') #Posiciona a string a esquerda, utilizando 20 caracteres de espaço
print(f'{saudacao:-^20}') #Posiciona a string no centro dos 20 caracteres, utilizando '-' para completar o resto do espaço em branco.

